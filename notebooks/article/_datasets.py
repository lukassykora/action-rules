"""Shared dataset configuration for the article reproducibility notebooks.

This module is *not* a notebook — it's the canonical source of truth for the
four benchmark datasets used across the article tables and figures:

- Telco Customer Churn
- IBM Employee Attrition
- UCI Bank Marketing
- Taiwan Credit Card Default

Each loader returns a :class:`DatasetConfig` dataclass with:

- ``df`` — the preprocessed pandas DataFrame
- attribute lists (``stable_attributes``, ``flexible_attributes``)
- target metadata (``target``, ``target_undesired_state``, ``target_desired_state``)
- utility tables (``intrinsic_utility_table``, ``transition_utility_table``)
- mining hyperparameters (``min_stable_attributes``, ``min_flexible_attributes``,
  ``min_undesired_support``, ``min_desired_support``,
  ``min_undesired_confidence``, ``min_desired_confidence``)

The hyperparameters match those used by the existing demo notebooks under
``notebooks/ci/``.  Article-grade results can be regenerated by importing
this module and calling each loader function — no inline preprocessing is
duplicated across the table/figure notebooks.
"""

from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path
from typing import Dict, List, Optional, Tuple

import pandas as pd

# ---------------------------------------------------------------------------
# Common data class
# ---------------------------------------------------------------------------


@dataclass
class DatasetConfig:
    """Bundle the data and mining configuration for a single benchmark dataset."""

    name: str
    df: pd.DataFrame
    stable_attributes: List[str]
    flexible_attributes: List[str]
    target: str
    target_undesired_state: str
    target_desired_state: str
    min_stable_attributes: int
    min_flexible_attributes: int
    min_undesired_support: int
    min_desired_support: int
    min_undesired_confidence: float
    min_desired_confidence: float
    intrinsic_utility_table: Optional[Dict[Tuple[str, str], float]] = None
    transition_utility_table: Optional[Dict[Tuple[str, str, str], float]] = None
    use_sparse_matrix: bool = False
    notes: str = ""


# ---------------------------------------------------------------------------
# Path resolution
# ---------------------------------------------------------------------------


def _repo_root() -> Path:
    here = Path(__file__).resolve()
    for parent in [here, *here.parents]:
        if (parent / 'pyproject.toml').exists():
            return parent
    raise FileNotFoundError("Could not locate repository root (no pyproject.toml found).")


def _ci_data_dir() -> Path:
    return _repo_root() / 'notebooks' / 'ci' / 'data'


def _legacy_data_dir() -> Path:
    return _repo_root() / 'notebooks' / 'data'


